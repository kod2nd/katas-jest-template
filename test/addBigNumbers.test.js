const { add } = require("../src/addBigNumbers");

test("should ", () => {
  expect(add("1", "1")).toBe("2");
});

test("should ", () => {
  expect(add("1000000000000000000000000000", "1000000000000000000000000000")).toBe("2000000000000000000000000000");
});

test("should ", () => {
  expect(add("9000000000000000000000000009", "9000000000000000000000000009")).toBe("18000000000000000000000000018");
});

